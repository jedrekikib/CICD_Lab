"""
This module has utility math functions for our CI/CD test repo.
"""


def add(a: int, b: int) -> int:
    """
    This function adds two variables.

            Parameters:
                    a (int): First integer
                    b (int): Second integer

            Returns:
                    sum (int): A sum of a and b.
    """
    return a + b


def subtract(a: int, b: int) -> int:
    """
    This function subtracts two variables.

            Parameters:
                    a (int): First integer
                    b (int): Second integer

            Returns:
                    subtraction result (int): A subtraction of a and b.
    """
    return a - b


def multiply(a: int, b: int) -> int:
    """
    This function multiplies two variables.

            Parameters:
                    a (int): First integer
                    b (int): Second integer

            Returns:
                    multiplication result (int): A multiplication of a and b.
    """
    return a * b


def divide(a: int, b: int) -> float:
    """
    This function divides two variables.

            Parameters:
                    a (int): First integer
                    b (int): Second integer

            Returns:
                    division result (int): A division of a and b.
    """
    return a / b
